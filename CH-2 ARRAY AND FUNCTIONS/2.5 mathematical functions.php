<?php
// Mathematical functions demonstration

// Absolute value of a number
echo "Absolute value of -5: " . abs(-5) . "\n";

// Round a number up to the nearest integer
echo "Ceiling of 4.2: " . ceil(4.2) . "\n";

// Round a number down to the nearest integer
echo "Floor of 4.8: " . floor(4.8) . "\n";

// Round a number to the nearest integer
echo "Rounded value of 4.5: " . round(4.5) . "\n";

// Generate a random number
echo "Random number between 1 and 10: " . rand(1, 10) . "\n";

// Get the maximum value among a list of numbers
echo "Maximum value among 5, 8, and 3: " . max(5, 8, 3) . "\n";

// Get the value of pi
echo "Value of pi: " . pi() . "\n";

// Calculate the power of a number
echo "2 raised to the power of 3: " . pow(2, 3) . "\n";

// Square root of a number
echo "Square root of 16: " . sqrt(16) . "\n";

// Exponential value
echo "Exponential value of 2: " . exp(2) . "\n";

// Natural logarithm
echo "Natural logarithm of 10: " . log(10) . "\n";

// Convert decimal to binary
echo "Binary representation of 10: " . decbin(10) . "\n";

// Convert decimal to octal
echo "Octal representation of 10: " . decoct(10) . "\n";

// Convert decimal to hexadecimal
echo "Hexadecimal representation of 10: " . dechex(10) . "\n";

// Trigonometric functions
echo "Sine of 30 degrees: " . sin(deg2rad(30)) . "\n";
echo "Cosine of 60 degrees: " . cos(deg2rad(60)) . "\n";
echo "Tangent of 45 degrees: " . tan(deg2rad(45)) . "\n";

// Convert degrees to radians
echo "45 degrees in radians: " . deg2rad(45) . "\n";

// Convert radians to degrees
echo "1 radian in degrees: " . rad2deg(1) . "\n";

